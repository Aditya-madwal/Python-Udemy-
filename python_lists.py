fruits = ["apple" , "mango" , "cherry" , "banana"]

#print(listname[indexno.])
print(fruits[2])

my_fav = fruits[1]
print(my_fav)

# negativeindex {-1 represents the last item}
last_fruit = fruits[-1]
print(last_fruit)

#changing an item

fruits[2] = "papaya"
print(fruits)

#append

fruits.append("strawberry")
print(fruits)

# insert
fruits.insert(2,"cherry")
print(fruits)

#remove
fruits.remove("papaya")
print(fruits)

